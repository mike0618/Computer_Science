# Gallons to Liters conversion function
def gallons_to_liters(g: float):
    print(f"{g} Gallons = {g * 3.78541:.2f} Liters")


# Liters to Gallons conversion function
def liters_to_gallons(lt: float):
    print(f"{lt} Liters = {lt * 0.264172:.2f} Gallons")


